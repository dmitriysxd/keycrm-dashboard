-- Migration 038: рекалібровані сезонні періоди під реальні дані юзера
--
-- Попередні значення (з міграції 030) були прикладом "по інтуїції":
-- 8 березня + Випускні + Новий рік = піки, Літо = спад. Реальність
-- юзера за 12 місяців 2025 року показала зовсім іншу картину:
--
--   місяць  | % факту | реальний множник (% / 8.33)
--   ──────────────────────────────────────────────────
--   січ     | 9.08    | 1.09   (норма+)
--   лют     | 6.88    | 0.83   (спад!)        ← було 1.5
--   бер     | 6.61    | 0.79   (спад!)        ← було 1.5/1.3
--   кві     | 6.02    | 0.72   (спад!)        ← було 1.3
--   тра     | 9.58    | 1.15   (пік!)         ← було 0.6
--   чер     | 8.41    | 1.01   (норма)        ← було 0.6
--   лип     | 9.31    | 1.12   (пік!)         ← було 0.6
--   сер     | 7.98    | 0.96   (норма)        ← було 0.6
--   вер     | 5.42    | 0.65   (найслабший!)  ← було 1.7
--   жов     | 9.58    | 1.15   (норма+)       ← було 1.7
--   лис     | 9.84    | 1.18   (норма+)       ← було 1.7
--   гру     | 11.27   | 1.35   (пік)          ← було 1.7/1.0
--
-- ВИСНОВКИ:
-- 1. "Літо опту слабке" — НЕВІРНО для цього бізнесу. Травень-липень = пік.
-- 2. "8 березня+випускні" — НЕВІРНО. Лютий-квітень = найслабший період року.
-- 3. "Жовтень-грудень" — частково. Реальний пік ВУЖЧИЙ: середина листопада +
--    весь грудень. Жовтень — лише помірний (1.15).
-- 4. Вересень — окремий найслабший місяць.
--
-- НОВІ ПЕРІОДИ:
--   Грудневий пік   16 лис – 31 гру  × 1.30
--   Літньо-весн пік  1 тра – 31 лип  × 1.15
--   Лют-кві спад     1 лют – 30 кві  × 0.75
--   Вересневий спад  1 вер – 30 вер  × 0.65
--   (інші місяці — серпень, жовтень, перша половина листопада, січень,
--    початок лютого — без періоду, тобто × 1.0)

-- Чистимо попередній набір. Юзер заводив його з міграції 030 як приклад.
-- Якщо хтось додав свої кастомні періоди — вони збережуться (видаляємо
-- тільки сидові імена).
DELETE FROM seasonal_periods
WHERE name IN (
  'Новий рік (опт)',
  '8 березня (опт)',
  'Випускні (опт)',
  'Літо (опт)'
);

-- Додаємо нові періоди на основі реальних даних 2025 року.
INSERT INTO seasonal_periods (name, start_mm_dd, end_mm_dd, multiplier, comment)
VALUES
  ('Грудневий пік (опт)',     '11-16', '12-31', 1.30,
   'Реальний пік року: середина листопада + увесь грудень. 35% піднесення відносно середнього.'),
  ('Літньо-весняний пік',     '05-01', '07-31', 1.15,
   'Травень/червень/липень — реальний другий пік (опт готується до сезону).'),
  ('Лютий-квітневий спад',    '02-01', '04-30', 0.75,
   'Лютий-квітень — найслабші місяці після свят. Опт не закупається.'),
  ('Вересневий спад',         '09-01', '09-30', 0.65,
   'Окремий найслабший місяць року. Між літом і передноворічним сезоном.');

